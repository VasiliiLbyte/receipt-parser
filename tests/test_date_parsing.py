#!/usr/bin/env python3
"""Тестирование парсинга дат"""

from src.openai_client import postprocess_data

def test_date_parsing():
    """Тестирование парсинга различных форматов дат"""
    print("🧪 Тестирование парсинга дат")
    print("=" * 50)
    
    test_cases = [
        # Стандартные форматы
        ("2025-12-31", "2025-12-31", "Стандартный ISO формат"),
        ("31.12.2025", "2025-12-31", "Русский формат ДД.ММ.ГГГГ"),
        ("31.12.25", "2025-12-31", "Русский формат с двузначным годом"),
        ("2025/12/31", "2025-12-31", "Формат с косой чертой"),
        ("31/12/2025", "2025-12-31", "Европейский формат"),
        ("31/12/25", "2025-12-31", "Европейский формат с двузначным годом"),
        
        # С разными разделителями
        ("31-12-2025", "2025-12-31", "Формат с дефисами"),
        ("31-12-25", "2025-12-31", "Формат с дефисами и двузначным годом"),
        ("2025.12.31", "2025-12-31", "Формат с точками"),
        
        # С лишними символами (как может прийти от OCR)
        ("[31.12.2025]", "2025-12-31", "Дата в скобках"),
        ('"31.12.2025"', "2025-12-31", "Дата в кавычках"),
        ("(31.12.2025)", "2025-12-31", "Дата в круглых скобках"),
        ("Дата: 31.12.2025", "2025-12-31", "Дата с префиксом"),
        ("31.12.2025 г.", "2025-12-31", "Дата с 'г.'"),
        ("31 декабря 2025", "2025-12-31", "Полное название месяца"),
        ("31 дек 2025", "2025-12-31", "Сокращенное название месяца"),
        
        # Проблемные случаи (возможные ошибки OCR)
        ("31.12.2025г", "2025-12-31", "Дата без пробела перед 'г'"),
        ("31,12,2025", "2025-12-31", "Дата с запятыми вместо точек"),
        ("31 12 2025", "2025-12-31", "Дата с пробелами"),
        ("2025-12-31 10:30:45", "2025-12-31", "Дата с временем"),
        ("31/12/2025 10:30", "2025-12-31", "Дата с временем (европейский формат)"),
        
        # OCR-ошибки в последней цифре года (основная проблема)
        ("15.10.24", "2024-10-15", "Год 24 → 2024"),
        ("15.10.25", "2025-10-15", "Год 25 → 2025"),
        ("03.05.23", "2023-05-03", "Год 23 → 2023"),
        ("01.01.22", "2022-01-01", "Год 22 → 2022"),
        # Полные годы которые не должны меняться
        ("15.10.2024", "2024-10-15", "Полный год 2024 не меняется"),
        ("15.10.2023", "2023-10-15", "Полный год 2023 не меняется"),
        ("15.10.2025", "2025-10-15", "Полный год 2025 не меняется"),
        
        # Некорректные даты (должны вернуть None)
        ("неверная дата", None, "Некорректная строка"),
        ("99.99.9999", None, "Некорректные числа"),
        ("31.13.2025", None, "Некорректный месяц"),
        ("32.12.2025", None, "Некорректный день"),
        ("", None, "Пустая строка"),
        (None, None, "None значение"),
    ]
    
    all_passed = True
    for input_date, expected, description in test_cases:
        print(f"\n📊 Тест: {description}")
        print(f"   Вход: '{input_date}'")
        
        # Создаем тестовые данные
        data = {"date": input_date}
        result = postprocess_data(data)
        actual = result.get("date")
        
        print(f"   Ожидалось: {expected}")
        print(f"   Получено: {actual}")
        
        # Для пустой строки: "" должно стать None
        if input_date == "" and actual is None:
            print("   ✅ OK (пустая строка → None)")
        elif actual == expected:
            print("   ✅ OK")
        else:
            print("   ❌ ОШИБКА")
            all_passed = False
    
    print("\n" + "=" * 50)
    print("📋 Анализ текущей логики:")
    
    # Проверяем, какие форматы поддерживаются
    from datetime import datetime
    
    current_formats = [
        "%Y-%m-%d", "%d.%m.%Y", "%d.%m.%y", "%Y/%m/%d", "%d/%m/%Y", 
        "%d/%m/%y", "%d-%m-%Y", "%d-%m-%y", "%Y.%m.%d", "%d %B %Y", "%d %b %Y"
    ]
    
    print(f"Поддерживаемых форматов: {len(current_formats)}")
    print("Примеры поддерживаемых форматов:")
    for fmt in current_formats[:5]:
        print(f"  - {fmt}")
    
    if not all_passed:
        print("\n⚠️  Некоторые тесты не прошли")
        print("💡 Нужно улучшить логику парсинга дат")
    
    return all_passed

